DROP TABLE IF EXISTS ALIMENTOS;

DROP SEQUENCE IF EXISTS ALIMENTOS_SEQ;

CREATE SEQUENCE "ALIMENTOS_SEQ"
	MINVALUE 15
	MAXVALUE 200
	INCREMENT BY 50
	START WITH 15
	NO CACHE
	NO CYCLE;

CREATE TABLE IF NOT EXISTS ALIMENTOS (
	ID INT PRIMARY KEY,
	NOMBRE VARCHAR(100),
	DESCRIPCION VARCHAR(100),
	CATEGORIA VARCHAR(100),
	PRECIO INT,
	FECHA_CADUCIDAD DATE
);